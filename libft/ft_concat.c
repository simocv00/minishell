#include "libft.h"

void	ft_concat(char *dest, char *src, int *i)
{
	int	j;

	while (dest[*i] && *i < 999)
		(*i)++;
	j = 0;
	while (src[j] && *i < 999)
		dest[(*i)++] = src[j++];
	dest[*i] = 0;
}
